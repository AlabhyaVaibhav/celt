										$faqItems.css('display', 'none');
										if( hashFaqSelector != 'all' ) {
											$( hashFaqSelector ).fadeIn(500);
										} else {
											$faqItems.fadeIn(500);
										}
									}
								}

								$('#portfolio-filter a').click(function(){
									$('#portfolio-filter li').removeClass('activeFilter');
									$(this).parent('li').addClass('activeFilter');
