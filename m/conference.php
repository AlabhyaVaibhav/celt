					<p>
						<strong>I. Bootstrapping vs Funding </strong><br><br>
						The session will encompass the various pros and cons of being a 
						bootstrapped or a funded startup. Seeing the unprecedented rise in the the 
						startup community in the last few years, people wonder which option to 
						go for.The luxury of freedom in a bootstrapped startup against the 
						accelerated growth of a funder one, we will be pondering over many such 
						aspects. 
						<br /> 
						<strong><br><br>
							II. The art of selling and negotiations.</strong><br><br> 
							Negotiating is a part of everyday life, but in business it's absolutely critical 
							to your success. Poor negotiation can cripple a company just as quickly as 
							losing key customers. Many people believe that negotiations are "all or 
							nothing", and that there has to be one winner and one loser. Nothing 
							could be farther from the truth. While the goal of negotiation is most 
							certainly getting what you want, the fact is that incorporate terms and 
							ideas from both parties. The session will open the horizon for this art , allow 
							it to unfurl and bring it to your hands and understanding. 
							<br > <strong><br><br>
							III. The creator’s code </strong>
							The truth is coders gravitate towards other coders. As Ben Parr, a former 
							editor at Mashable, stated, "You have to code, not because you need to be 
							good at it, but because technical employees are far more likely to follow a 
							founder with technical experience." Here we will be discussing on the 
							essential coding skills that one has to possess to be a great entrepreneur. 
							Knowing the basics reassures potential investors and earns the respect of 
							the techies you need to join your team. 
							<br> <strong><br><br>
							IV.  Disrupting Cash – The changing Payment Landscape </strong> <br><br>
							The advent of internet has revolutionised personal finance and the overall 
							way payments are made. Online banking has almost replaced the traditional 
							banking systems. Originally intended as a way to carry ecash, digital wallets 
							have evolved into a way for smartphone and internet users to make 
							purchases, receive electronic receipts, and access o ers, coupons, loyalty 
							rewards, and product information. And with digital currency options like “Bitcoin”, one can visualise a day when banks will no longer be needed. 
							In this session, we discuss the ongoing trends of how payments are done 
							and what does it hold for upcoming and existing enterprises. <br><strong><br><br>

							V. Financial Analytics </strong> <br><br>
							A company’s financial data provides a provides an insight into the 
							company’s performance.It is imperative for the growth of a company not 
							only because it touches all parts of the company, but because it is 
							particularly useful in the context of profitability-a very important component 
							of business success and performance management. To fully understand 
							the factors driving the business requires timely financial information 
							combined with data from across the company value chain. <br><strong><br><br>

							VI. PRODUCT DEVELOPMENT AND INNOVATION </strong><br><br>
							Innovation has become critical to the long-term health of a business. No 
							longer can accompany rest on its laurels and keep doing “business as usual”. 
							The first and most important reason for any new development is to provide 
							new value to the customer. This new and increasing value is what keeps 
							companies growing. New products and services are the lifeblood of any 
							company. Hence the potential of innovative product development should 
							be identified. 

