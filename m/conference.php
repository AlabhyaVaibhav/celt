				<strong>I. IDEATION: HOW TO CREATE YOUR INNOVATIVE AND CREATIVE 
					BUSINESS PLAN </strong><br><br>
					A business plan is a tool for understanding how your business is put together. 
					It is used to monitor progress and decide the business’s fate. Writing out your 
					business plan forces you to review everything at once: your value proposition, 
					marketing assumptions, operations plan, financial plan and stating plan. 
					You'll end up spotting connections you otherwise would have missed. 
					Hence it is very important to have an innovative and creative business 
					plan-it’s a tool for employees and investors alike. <br /> 
					<strong><br><br>
						II. Product Designing and Prototyping</strong><br><br> 
						Nothing brings you closer to the functionality of the final product than 
						prototyping. While wireframes sketch out the blueprint and mockups show 
						the feel and texture of the design, it is the prototype that brings to life the 
						“experience” behind “user experience.The idea behind this session is to 
						discuss how essential is a prototype of a product, its advantages and how 
						to get started with envisioning your idea in a multi-dimensional field of view. 
						<br > <strong><br><br>
						III. Business Intelligence and Valuation </strong>
						The concept of business intelligence (BI) is used to refer to various 
						processes, products, techniques, or tools that enhance the decision making 
						process. Using BI tools, a company can anticipate and understand various 
						phenomena and trends in its market areas and, also, learn more about its 
						customer’s preferences and competitors actions. Over the past few years, 
						the Business Intelligence and Analytics space has seen strong exit activity 
						from internet, mobile, and software based startups. It’s time to learn how 
						Business Intelligence can propel your business toward success. 
						<br> <strong><br><br>
						IV. A. Open Source Vs Patents</strong> <br><br>
						Open Source is a widely used word in the world of computer technology 
						and while it started out as a term in the context of software development, 
						today it has gone beyond the 0s and 1s. Open source projects, products, or 
						initiatives are those that embrace and celebrate open exchange, 
						collaborative participation, rapid prototyping, transparency, meritocracy, and 
						above all community development. But is open source the right way to move 
						forward or is it yet another idealistic movement that is fundamentally 
						flawed? This session will explore these concepts and help you get better 
						grasp on what is right for your business. <br><strong><br><br>

						IV. B. Disruptive Innovation Vs Incremental Innovation</strong> <br><br>
						A disruptive innovation is an innovation that creates value network and 
						eventually disrupts an existing market and value network, displacing 
						established market leaders and alliances. Whereas incremental innovations 
						involve modest changes to existing products and services. 
						These are enhancements that keep a business competitive, such as new 
						product features and service improvements. <br><strong><br><br>

						V. Storytelling and PR for startups </strong><br><br>
						“Apple tried to use evidence to persuade IT execs and big companies to 
						adopt the Mac during the 80s”, explains Seth Godin, the king of marketing 
						and the ultimate storyteller. “They tried ads and studies that proved the 
						Mac was easier and cheaper to support. They failed. It was only the gentle 
						persistence of storytelling and the elevation of evangelists that turned 
						the tide”. Stories have power. They delight, enchant, touch, teach, recall, 
						inspire, motivate, challenge. They help us understand. The session will 
						concentrate on storytelling for uplifting the public relations of a startup 
						or company its relevance in business. 
						<br > <strong><br><br>
						VI. Product performance and channelization </strong><br><br>
						How well does a product perform and how to check if your product is giving 
						you the output you require. Also how to assess and work upon the customer 
						feedback that you receive. 
