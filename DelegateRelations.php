	 <b>INSTRUCTIONS FOR VISA APPLICATION:</b></font>
	 <br>
	 <br>

A. Process for filling up online application form: <br>

1. Applicant shall access the Visa Application system online through the 

following link: http://indianvisaonline.gov.in/visa/.<br>

2. Applicant visits the above mentioned web link and selects the Indian Mission from 

where he intends to apply for the Visa from the tab “Select Mission”.<br>

3. After furnishing the information exactly in the manner asked for in the forms, 

especially the names, address and date of birth; verify the application details before 

submitting the online application.<br>

4. The automatic Application Id generated after submitting the form is used for tracking 

and further communications<br>

NOTE: Once the online application form is submitted by the applicant, then 

further modifications are not allowed. Hence applicants are requested to 

check and validate the details before submitting the online application form.<br>

5. Applicants can either pay the visa fee online or approach the concerned Indian 

Mission and submit the hard copy with the required documents.<br>

6. If the applicant is paying online then the system automatically takes the link to 

perform the following:<br>

i. Selection of Date and Time of appointment at the concerned Indian Mission<br>

ii. Calculation of Visa fee, service charge, VAT etc. as applicable according to the Visa 

type<br>

2. In case the facility of Appointment Scheduling and e-Payment is not available for the 

concerned Indian mission, then applicant has to take the hardcopy of the online 

application form and visit the concerned Indian Mission to submit the same along 

with supporting documents and make the payment.<br>

NOTE: The e-payment facility is not available for all the Indian Missions abroad. 

Wherever the said facility is not available, it is requested to kindly submit the 

application submission fees to either the out sourcing agency or to the Mission 

directly along with the supporting documents.<br>
