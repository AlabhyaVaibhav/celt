	<h4><span>Eligibility </span></h4>
<p>
Tourist visa on Arrival would be issued subject to the following conditions:

International Travellers whose sole objective of visiting India is recreation , sight-seeing , 

casual visit to meet friends or relatives, short duration medical treatment or casual business 

visit.

Passport should have at least six months validity.

International Travellers should have return ticket or onward journey ticket, with sufficient 

money to spend during his/her stay in India.

International Travellers having Pakistani Passport or Pakistani origin may please apply for 

regular Visa at Indian Mission.

Not available to Diplomatic/Official Passport Holders. 
