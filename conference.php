									company with a proper examination of a broad array of law-sensitive issues 
									relating to intellectual property, international trade, the duties of directors and 
									o icers, business disputes, and bankruptcy and reorganization and other 
									must-know legal concepts for business owners. The goal is not to impart 
									technical legal skills, but to enhance the judgment which people will bring 
