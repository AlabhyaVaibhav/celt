										II.  Branding and Ad-Making</strong><br><br> 
										When the market is saturated with businesses that run on closely similar 
										models and provide similar services or products, how will your target customer 
										identify with you? This is where branding steps in. It's primary objective is to 
										establish awareness and preference for your product or service. AdMaking 
										and Branding are an art that involve knowing your audience, identifying 
										the key benefits and playing your cards right to build a strong relationship 
										with your customers. Delve in to know more about the art of branding and 
										advertising. 



										<br > <strong><br><br>
										III. A. Red Ocean Innovation Vs. Blue Ocean Innovation 
									</strong>
									Red oceans are all the industries in existence today – the known market 
									space, where industry boundaries are defined and companies try to 
									outperform their rivals to grab a greater share of the existing market. Cut 
									throat competition turns the ocean bloody red. Hence, the term ‘red’ oceans. 
									Blue oceans denote all the industries not in existence today – the unknown 
									market space, unexplored and untainted by competition. Like the ‘blue’ 
									ocean, it is vast, deep and powerful –in terms of opportunity and profitable 
									growth.So, do you want to beat the competition or make it irrelevant? Join 
									us in this session to know more about how these things work.
									<br> <strong><br><br>
									IV. B. Start-up Legalities and IPR </strong> <br><br>
